function H=Hyper(data,kNN)
m = size(data,1);
    H = zeros(m,m);    %H?incidence matrix;
    for j = 1:m              %??KNN?????????????????K????????
        z(j,:) = knnsearch(data,data(j,:),'dist','hamming','K',kNN);  %knn??K????De?????
